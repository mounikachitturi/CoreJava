public class DOUBd {
    public static void main(String[] args)
    {
        int myIntvalue =5/3;
        double myDoulevalue =5d/3d;
        float myFloatvalue =3f/3f;
        System.out.println(" Int " + myIntvalue );
        System.out.println(" double " + myDoulevalue);
        System.out.println(" float " + myFloatvalue);
        double numPound =200d;
        double covertedintoKilograms = numPound*0.234198d;
        System.out.println(" Kilogram " + covertedintoKilograms);
    }
}
